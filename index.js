//add categories automaticaly
function displayCategories(data){
    const container = document.querySelector('.cat');

    data.forEach( n => {
        const btn = document.createElement('button');
        btn.setAttribute('data-category', n.cat_name);
        btn.id = `cat${n.cat_id}`;
        btn.innerHTML = `${n.icon}<p>${n.cat_name}</p>`;
        container.appendChild(btn);
    });
}

displayCategories(data);

//Open the game
const container = document.querySelector('.container');
const btns = document.querySelectorAll('button');

btns.forEach(btn => {
    btn.addEventListener('click',function(){
        const category = btn.getAttribute('data-category');
        location.href = `game.html?category=${encodeURIComponent(category)}`;
    })
})